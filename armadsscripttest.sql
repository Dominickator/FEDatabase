CREATE DATABASE IF NOT EXISTS armadsDB;
USE armadsDB;

DROP TABLE IF EXISTS Weapons;
DROP TABLE IF EXISTS Game;

CREATE TABLE Game (
	number INT NOT NULL,
    name VARCHAR(255),
    PRIMARY KEY (number)
);

CREATE TABLE Weapons (
	name VARCHAR(255) NOT NULL,
    number INT NOT NULL,
    might INT,
    effect VARCHAR(255),
    hit FLOAT,
    crit FLOAT,
	weight INT,
    durability INT,
    is_legendary BOOL,
	char_name VARCHAR(255),
    PRIMARY KEY (name),
    FOREIGN KEY (number) REFERENCES Game(number)
);

